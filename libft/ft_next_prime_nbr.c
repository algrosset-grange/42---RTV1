int	ft_next_prime_nbr(int nb)
{
	int i;

	i = 2;
	if (nb < 2)
		return (2);
	while (i * i < nb)
	{
		if (nb % i == 0 && nb != i)
		{
			nb++;
			i = 2;
		}
		else
			i++;
	}
	return (nb);
}
